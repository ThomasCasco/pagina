  // Función para simular una operación asíncrona
  function simulateAsyncOperation(duration) {
    return new Promise((resolve) => setTimeout(resolve, duration))
  }
  
  // Función para manejar el inicio de sesión
  async function handleLogin(event) {
    event.preventDefault()
    showLoading()
  
    const legajo = document.getElementById("legajo").value
    const documento = document.getElementById("documento").value
    const user = users.find((u) => u.legajo === legajo && u.documento === documento)
  
    await simulateAsyncOperation(1000) // Simula una operación de red
  
    if (user) {
      localStorage.setItem("currentUser", JSON.stringify(user))
      window.location.href = "login.html" //login.html
    } else {
      document.getElementById("errorMessage").textContent = "Credenciales inválidas. Por favor, intente de nuevo."
      hideLoading()
    }
  }
  
  // Función para mostrar el mensaje de bienvenida en la página de inicio
  function showWelcomeMessage() {
    const currentUser = JSON.parse(localStorage.getItem("currentUser"))
    if (currentUser) {
      document.getElementById("welcomeMessage").textContent = `Hola, ${currentUser.nombre}`
    } else {
      window.location.href = "index.html"//index.html
    }
  }
  
